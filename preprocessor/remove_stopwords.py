html_entities_set = set(['nbsp', 'lt', 'gt', 'amp', 'quot', 'apos', 'cent', 'pound', 'yen', 'euro'])
en_punctuations_set = set('/,.?<>-+!`@#$%^&*()[]{};:"\'')
en_stopwords_set = set(['i', 'me', 'my', 'myself', 'we', 'our', 'ours', 'ourselves', 'you', 'your', 'yours', 'yourself', 'yourselves', 'he', 'him', 'his', 'himself', 'she', 'her', 'hers', 'herself', 'it', 'its', 'itself', 'they', 'them', 'their', 'theirs', 'themselves', 'what', 'which', 'who', 'whom', 'this', 'that', 'these', 'those', 'am', 'is', 'are', 'was', 'were', 'be', 'been', 'being', 'have', 'has', 'had', 'having', 'do', 'does', 'did', 'doing', 'a', 'an', 'the', 'and', 'but', 'if', 'or', 'because', 'as', 'until', 'while', 'of', 'at', 'by', 'for', 'with', 'about', 'against', 'between', 'into', 'through', 'during', 'before', 'after', 'above', 'below', 'to', 'from', 'up', 'down', 'in', 'out', 'on', 'off', 'over', 'under', 'again', 'further', 'then', 'once', 'here', 'there', 'when', 'where', 'why', 'how', 'all', 'any', 'both', 'each', 'few', 'more', 'most', 'other', 'some', 'such', 'no', 'nor', 'not', 'only', 'own', 'same', 'so', 'than', 'too', 'very', 's', 't', 'can', 'will', 'just', 'don', 'should', 'now', 'd', 'll', 'm', 'o', 're', 've', 'y', 'ain', 'aren', 'couldn', 'didn', 'doesn', 'hadn', 'hasn', 'haven', 'isn', 'ma', 'mightn', 'mustn', 'needn', 'shan', 'shouldn', 'wasn', 'weren', 'won', 'wouldn'])
regex_num = re.compile(r'^(?=.)([+-]?([\d]*)(\.([\d]+))?)$')
regex_alpha = re.compile(r'^[a-zA-Z]$')
regex_list_mark = re.compile(r'^[a-zA-Z\d]\.\s*$')

def remove_stopwords_english(
	tokens, 
	with_punctuations=True,
	with_html_tags=True,
	with_html_entities=True,
	with_num=True, 
	with_alpha=True,
	with_list_mark=True):
	
	l = []
	for token in tokens:
		token = str(token).lower()
		if token in en_stopwords_set:
			continue
		if with_punctuations and token in en_punctuations_set:
			continue
		if with_html_tags and token in html_tags_set:
			continue
		if with_html_entities and token in html_entities_set:
			continue
		if with_num and regex_num.match(token):
			continue
		if with_alpha and regex_alpha.match(token):
			continue
		if with_list_mark and regex_list_mark.match(token):
			continue
		l.append(token)
	return l